    private void signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupActionPerformed
        try {

            Connection con;
            con = ConnectMSSQL.connectDB();
            Statement st = con.createStatement();
            String query = "INSERT INTO Students (StudentId,FirstName,LastName,Cell,Address,Password) VALUES(?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(query);

            ps.setString(1, id.getText());
            ps.setString(2, firstname.getText());
            ps.setString(3, lastname.getText());
            ps.setString(4, cell.getText());
            ps.setString(5, address.getText());
            ps.setString(6, pass.getText());

            ps.executeUpdate();
            
            id.setText("");
            firstname.setText("");
            lastname.setText("");
            cell.setText("");
            address.setText("");
            pass.setText("");

            int a = JOptionPane.showConfirmDialog(this, "Sign Up?", "Select", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                new StudentHome().setVisible(true);
                this.dispose();
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_signupActionPerformed

